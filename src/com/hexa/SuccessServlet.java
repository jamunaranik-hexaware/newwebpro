	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// TODO Auto-generated method stub
		PrintWriter pw=response.getWriter();
		String s=(String)request.getAttribute("uname");
		HttpSession session =request.getSession();
		String s1=(String)session.getAttribute("uname");
		
		ServletContext context=getServletContext();
		String s2=(String)context.getAttribute("uname");
		pw.print("Welcome-request "+s +"!!");
		pw.print("Welcome-session "+s1 +"!!");
		pw.print("Welcome-context "+s2 +"!!");
		pw.println("<a href='NewServlet'>NewServlet</a>");
		// RequestDispatcher rd=request.getRequestDispatcher("NewServlet");
			//rd.forward(request, response);
	}

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse response)
	 */
